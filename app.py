# Helper function for bad rate plots by categorical variable
def generate_bad_rate_plot(df, column_name):
    # Explicitly set observed=False to avoid FutureWarning with categorical data
    bad_rates_table = pd.pivot_table(
        df, values='custid', index=column_name, columns='bad',
        aggfunc='count', fill_value=0, observed=False
    )
    bad_rates_table['Total'] = bad_rates_table.sum(axis=1)
    bad_rates_table['Bad Rate'] = (bad_rates_table[1] / bad_rates_table['Total']) * 100

    fig = px.bar(
        bad_rates_table.reset_index(),
        x=column_name, y='Bad Rate',
        title=f'Bad Rate by {column_name.replace("_", " ").title()}',
        labels={'Bad Rate': 'Bad Rate (%)'},
        color_discrete_sequence=px.colors.qualitative.Plotly
    )
    fig.update_layout(title_x=0.5)
    return fig, bad_rates_table

