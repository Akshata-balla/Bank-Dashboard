@app.callback(
    Output('dynamic-wordcloud', 'src'),
    Input('sentiment-filter', 'value')
)
def update_wordcloud(selected_sentiment):

    # Filter reviews based on dropdown
    if selected_sentiment == 'All':
        filtered_df = nps_df
    else:
        filtered_df = nps_df[nps_df['Sentiment'] == selected_sentiment]

    # If no reviews, return empty image
    if filtered_df.empty:
        return wordcloud_img_src

    # Generate text for word cloud
    text = ' '.join(filtered_df['Review'].astype(str))

    stop_words_wc = set(stopwords.words('english'))
    stop_words_wc.update(['bank', 'loan', 'process'])

    processed_text = " ".join(
        word for word in text.split()
        if word.lower() not in stop_words_wc and word.isalpha()
    )

    wc = WordCloud(
        width=800,
        height=400,
        background_color='white'
    ).generate(processed_text)

    buf = io.BytesIO()
    plt.figure(figsize=(10, 5))
    plt.imshow(wc, interpolation='bilinear')
    plt.axis('off')
    plt.savefig(buf, format='png')
    plt.close()

    encoded_img = base64.b64encode(buf.getvalue()).decode()
    return f"data:image/png;base64,{encoded_img}"

