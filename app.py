def update_eda_graph(selected_plot_type, selected_variable):
    if not selected_variable:
        return go.Figure()

    fig = go.Figure()
    if selected_plot_type == 'categorical_bad_rate':
        fig, _ = generate_bad_rate_plot(data, selected_variable)
    elif selected_plot_type == 'numeric_box_whisker':
        # Create Box-Whisker plot for numeric variable by 'bad' status
        temp_data = data.dropna(subset=[selected_variable, 'bad'])
        fig = px.box(temp_data, x='bad', y=selected_variable,
                     title=f'Box-Whisker Plot of {selected_variable.replace("_", " ").title()} by Bad Status')
        fig.update_xaxes(tickvals=[0, 1], ticktext=['Non-Defaulter', 'Defaulter'])
        fig.update_layout(title_x=0.5)
    elif selected_plot_type == 'numeric_mean_median':
        # Calculate mean and median for numeric variable by 'bad' status
        temp_data = data.dropna(subset=[selected_variable, 'bad'])
        stats = temp_data.groupby('bad')[selected_variable].agg(['mean', 'median'])
        fig = px.bar(
            stats.reset_index(),
            x='bad', y=['mean', 'median'], barmode='group',
            title=f'Mean and Median of {selected_variable.replace("_", " ").title()} by Bad Status',
            labels={'bad': 'Bad Status', 'value': 'Value', 'variable': 'Statistic'}
        )
        fig.update_xaxes(tickvals=[0, 1], ticktext=['Non-Defaulter', 'Defaulter'])
        fig.update_layout(title_x=0.5)
    elif selected_plot_type == 'heatmap':
        if selected_variable == 'gender_ms':
            fig = fig_heatmap_gender_ms
        elif selected_variable == 'house_child':
            fig = fig_heatmap_house_child

    return fig

